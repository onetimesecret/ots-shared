# SSH config generation
# ---------------------------------------------------------------------------

SSH_DIR = ".ssh"
SSH_CONFIG_FILENAME = "config"
SSH_KNOWN_HOSTS_FILENAME = "known_hosts"


def generate_ssh_config(
    marker: dict,
    env_name: str,
    ssh_keyfile: str,
    ssh_port: int = 22,
    username: str = "debian",
) -> str:
    """Generate SSH config content for an OTS environment.

    Args:
        marker: Loaded otsinfra.yaml data (must contain ``hosts:`` block).
        env_name: Environment name prefix for hostnames (e.g. "eu").
        ssh_keyfile: Path to SSH private key (typically ``.trust/deploy/ssh``).
        ssh_port: SSH port (default: 22).
        username: SSH username (default: debian).

    Returns:
        SSH config file content as a string.

    The jumphost gets a placeholder ``<PUBLIC_IP_AFTER_DEPLOY>`` for its
    HostName since the public IP is only known after ``lots deploy``.
    Other hosts use their private_ip_address from the marker and ProxyJump
    through the jumphost.
    """
    hosts = marker.get("hosts", {})
    if not isinstance(hosts, dict):
        hosts = {}

    ordinal = "01"

    lines = [
        f"# Auto-generated by lots init — ENV_NAME={env_name}",
        "",
    ]

    # Jumphost first (no ProxyJump, public IP placeholder)
    if "jumphost" in hosts:
        lines.extend(
            [
                f"Host {env_name}-jumphost-{ordinal}",
                "  HostName                <PUBLIC_IP_AFTER_DEPLOY>",
                "",
            ]
        )

    # Other hosts: private IP + ProxyJump through jumphost
    for role in hosts:
        if role == "jumphost":
            continue
        ip = get_host_ip(marker, role, ordinal)
        hostname = ip if ip else f"<{role.upper()}_PRIVATE_IP>"
        lines.extend(
            [
                f"Host {env_name}-{role}-{ordinal}",
                f"  HostName                {hostname}",
                f"  ProxyJump               {env_name}-jumphost-{ordinal}",
                "",
            ]
        )

    # Wildcard block with common settings
    lines.extend(
        [
            f"Host {env_name}-*",
            f"  IdentityFile              {ssh_keyfile}",
            f"  UserKnownHostsFile        {{SSH_CONFIG_DIR}}/{SSH_KNOWN_HOSTS_FILENAME}",
            f"  Port                      {ssh_port}",
            f"  User                      {username}",
            "  StrictHostKeyChecking     accept-new",
            "  SendEnv                   -LANG -LC_*",
            "  UseKeychain               yes",
            "  IdentityAgent             none",
            "  IdentitiesOnly            yes",
            "",
        ]
    )

    return "\n".join(lines)


def create_ssh_config(
    directory: Path,
    marker: dict,
    env_name: str,
    *,
    force: bool = False,
) -> Path:
    """Create SSH config and known_hosts in *directory*/.ssh/.

    Args:
        directory: Environment directory (will create .ssh/ inside).
        marker: Loaded otsinfra.yaml data.
        env_name: Environment name prefix for hostnames.
        force: Overwrite existing files if True.

    Returns:
        Path to the created SSH config file.

    The deploy keypair path is derived from the standard location
    (``.trust/deploy/ssh``) rather than passed as an argument — init
    generates it there, and the SSH config references it directly.

    Reads ``$SSH_PORT`` and ``$DEPLOY_USER`` from environment if set,
    otherwise uses defaults (22 and "debian").

    Raises:
        FileExistsError: If config already exists and *force* is False.
    """
    ssh_dir = directory / SSH_DIR
    ssh_dir.mkdir(exist_ok=True)

    config_path = ssh_dir / SSH_CONFIG_FILENAME
    if config_path.exists() and not force:
        raise FileExistsError(f"{config_path} already exists (use --force to overwrite)")

    # Deploy keypair lives at .trust/deploy/ssh (generated by init)
    deploy_keyfile = directory / ".trust" / "deploy" / "ssh"

    # Read from env vars with defaults
    ssh_port = int(os.environ.get("SSH_PORT", "22"))
    username = os.environ.get("DEPLOY_USER", "debian")

    # Generate config with actual paths and env-derived values
    content = generate_ssh_config(
        marker, env_name, str(deploy_keyfile), ssh_port=ssh_port, username=username
    )
    # Replace placeholder with actual path
    content = content.replace("{SSH_CONFIG_DIR}", str(ssh_dir))

    config_path.write_text(content)

    # Create empty known_hosts
    known_hosts_path = ssh_dir / SSH_KNOWN_HOSTS_FILENAME
    if not known_hosts_path.exists() or force:
        known_hosts_path.touch()

    return config_path


