def _walk_up(filename: str, start: Path | None = None) -> Path | None:
    """Walk up from *start* looking for *filename*.

    Stops at the first directory containing .git or at the user's home
    directory — whichever is reached first. Returns None if not found.
    """
    current = (start or Path.cwd()).resolve()
    home = Path.home().resolve()

    while True:
        candidate = current / filename
        if candidate.is_file():
            return candidate

        # Stop at .git boundary
        if (current / ".git").exists():
            return None

        # Stop at home directory ceiling
        if current == home:
            return None

        parent = current.parent
        # Filesystem root — stop
        if parent == current:
            return None

        current = parent


def find_marker(start: Path | None = None) -> Path | None:
    """Walk up looking for ``otsinfra.yaml``, then ``.otsinfra.env``.

    Prefers the YAML marker. Falls back to the legacy env file so
    existing environments continue to work during migration.
    """
    yaml_path = _walk_up(MARKER_FILENAME, start)
    if yaml_path is not None:
        return yaml_path
    return _walk_up(ENV_FILENAME, start)


def find_env_file(start: Path | None = None) -> Path | None:
    """Walk up from *start* looking for a .otsinfra.env file.

    Stops at the first directory containing .git or at the user's home
    directory — whichever is reached first. Returns None if not found.

    .. deprecated:: Use :func:`find_marker` for environment directory
       discovery. This function is retained for code that specifically
       needs the KEY=VALUE env file.
    """
    return _walk_up(ENV_FILENAME, start)


def load_marker(path: Path) -> dict:
    """Load an ``otsinfra.yaml`` marker file.

    Returns a dict of the YAML contents. Uses a minimal safe loader
    that doesn't require PyYAML — the file format is simple enough
    for basic KEY: VALUE parsing. Falls back to PyYAML if available.
    """
    if not path.is_file():
        return {}

    text = path.read_text()

    # Try PyYAML first (preferred, handles all YAML)
    try:
        import yaml

        return yaml.safe_load(text) or {}
    except ImportError:
        pass

    # Minimal fallback: parse "key: value" lines with basic indentation
    # support for the nested hosts structure generated by generate_marker.
    result: dict = {}
    current_section: str | None = None
    current_key: str | None = None
    for raw_line in text.splitlines():
        stripped = raw_line.rstrip()
        if not stripped or stripped.lstrip().startswith("#"):
            continue
        if ":" not in stripped:
            continue
        indent = len(stripped) - len(stripped.lstrip())
        key, _, value = stripped.strip().partition(":")
        key = key.strip()
        value = value.strip().strip("'\"")
        if indent == 0:
            if not value:
                # Top-level section (e.g. "hosts:")
                current_section = key
                result[key] = {}
                current_key = None
            else:
                result[key] = value
                current_section = None
                current_key = None
        elif indent >= 2 and current_section and isinstance(result.get(current_section), dict):
            if indent < 4 or current_key is None:
                # Second-level key (e.g. "  db:")
                current_key = key
                if value:
                    result[current_section][key] = value
                else:
                    result[current_section][key] = {}
            else:
                # Third-level attribute (e.g. "    private_ip_address: 10.0.0.11")
                parent = result[current_section].get(current_key)
                if not isinstance(parent, dict):
                    result[current_section][current_key] = {}
                result[current_section][current_key][key] = value
    return result


_YAML_SPECIAL_CHARS = set(":#{}[]|>&*!%@'\",")


def _yaml_quote(value: str) -> str:
    """Quote a value for safe YAML output if it contains special characters."""
    if any(c in value for c in _YAML_SPECIAL_CHARS):
        # Use single quotes, escaping embedded single quotes by doubling
        return "'" + value.replace("'", "''") + "'"
    return value


def generate_marker(
    environment: str,
    *,
    hosts: dict[str, dict[str, str]] | None = None,
    **metadata: str,
) -> str:
    """Generate ``otsinfra.yaml`` content.

    Returns YAML text with ``env_name`` and ``created`` fields,
    plus any additional metadata key-value pairs. The signature
    parameter is still named ``environment`` for backward compatibility
    with ``lots init`` / ``pots init`` callers — the field on disk is
    written as ``env_name:`` per the issue #59 follow-on contract
    (single source of truth for ``parse_hostname``'s env validation).

    When *hosts* is provided, a ``hosts:`` block is appended keyed by
    role (e.g. ``db``, ``web``) with nested attributes like
    ``private_ip_address``.
    """
    lines = [
        f"env_name: {_yaml_quote(environment)}",
        f"created: '{date.today().isoformat()}'",
    ]
    for key, value in metadata.items():
        lines.append(f"{key}: {_yaml_quote(str(value))}")
    if hosts:
        lines.append("hosts:")
        for role, attrs in hosts.items():
            lines.append(f"  {role}:")
            for attr_key, attr_value in attrs.items():
                lines.append(f"    {attr_key}: {_yaml_quote(str(attr_value))}")
    lines.append("")
    return "\n".join(lines)


def create_marker(
    directory: Path,
    environment: str,
    *,
    force: bool = False,
    hosts: dict[str, dict[str, str]] | None = None,
    **metadata: str,
) -> Path:
    """Create ``otsinfra.yaml`` in *directory*.

    Returns the path to the created file.

    Raises:
        FileExistsError: If the marker already exists and *force* is False.
    """
    marker_path = directory / MARKER_FILENAME
    if marker_path.exists() and not force:
        raise FileExistsError(f"{marker_path} already exists (use --force to overwrite)")
    content = generate_marker(environment, hosts=hosts, **metadata)
    marker_path.write_text(content)
    return marker_path


