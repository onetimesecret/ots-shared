# Allowlisted AST node types for ``private_ip_formula`` evaluation. Anything
# not in this set (function calls, attribute access, subscripts, lambdas,
# comprehensions, etc.) is rejected before evaluation.
_FORMULA_ALLOWED_NODES: frozenset[type[ast.AST]] = frozenset(
    {
        ast.Expression,
        ast.BinOp,
        ast.UnaryOp,
        ast.Constant,
        ast.Name,
        ast.Load,
        ast.Add,
        ast.Sub,
        ast.Mult,
        ast.Div,
        ast.Mod,
        ast.FloorDiv,
        ast.LShift,
        ast.RShift,
        ast.USub,
        ast.UAdd,
    }
)


def _formula_walk(node: ast.AST, variables: Mapping[str, int | str]) -> int | str:
    """Recursive evaluator for the validated AST.

    Implements the operators in :data:`_FORMULA_ALLOWED_NODES`. Calls into
    Python operators directly rather than going through ``eval``/
    ``compile`` so the evaluator has no path to runtime code execution.
    """
    if isinstance(node, ast.Expression):
        return _formula_walk(node.body, variables)
    if isinstance(node, ast.Constant):
        # Already type-checked in _eval_formula's pre-walk pass.
        return node.value  # type: ignore[no-any-return]
    if isinstance(node, ast.Name):
        return variables[node.id]
    if isinstance(node, ast.UnaryOp):
        operand = _formula_walk(node.operand, variables)
        if not isinstance(operand, int):
            raise ValueError(f"unary operator requires int operand, got {type(operand).__name__}")
        if isinstance(node.op, ast.USub):
            return -operand
        if isinstance(node.op, ast.UAdd):
            return +operand
        raise ValueError(f"unsupported unary op: {type(node.op).__name__}")
    if isinstance(node, ast.BinOp):
        left = _formula_walk(node.left, variables)
        right = _formula_walk(node.right, variables)
        op = node.op
        # String concatenation with + is the only mixed-type operation
        # we permit; everything else requires both sides be int.
        if isinstance(op, ast.Add):
            if isinstance(left, str) or isinstance(right, str):
                return f"{left}{right}"
            return left + right
        if not (isinstance(left, int) and isinstance(right, int)):
            raise ValueError(
                f"{type(op).__name__} requires int operands, got "
                f"{type(left).__name__} and {type(right).__name__}"
            )
        if isinstance(op, ast.Sub):
            return left - right
        if isinstance(op, ast.Mult):
            return left * right
        if isinstance(op, ast.Div):
            return left // right  # integer division for IP arithmetic
        if isinstance(op, ast.FloorDiv):
            return left // right
        if isinstance(op, ast.Mod):
            return left % right
        if isinstance(op, ast.LShift):
            return left << right
        if isinstance(op, ast.RShift):
            return left >> right
        raise ValueError(f"unsupported binary op: {type(op).__name__}")
    raise ValueError(f"unreachable: unallowlisted node {type(node).__name__}")


def _eval_formula(formula: str, variables: Mapping[str, int | str]) -> str:
    """Evaluate a restricted arithmetic expression for CIDR-IP derivation.

    Supports the operators ``+ - * / // % << >>`` plus parenthesised
    sub-expressions, integer/string constants, and the names supplied in
    *variables* (typically ``ordinal``, ``region_id``, ``env_name``).

    Rejects function calls, attribute access, subscripts, lambdas,
    comprehensions, and any other node type with :class:`ValueError`
    naming the offending node — fail loud so a typo in the operator's
    formula surfaces immediately rather than silently evaluating to a
    wrong (but valid) IP. Implementation walks the validated AST
    directly; no use of ``eval``/``compile``.
    """
    try:
        tree = ast.parse(formula, mode="eval")
    except SyntaxError as exc:
        raise ValueError(f"private_ip_formula syntax error: {formula!r} ({exc})") from exc

    for node in ast.walk(tree):
        if type(node) not in _FORMULA_ALLOWED_NODES:
            raise ValueError(
                f"private_ip_formula contains forbidden node {type(node).__name__!s}: {formula!r}"
            )
        if isinstance(node, ast.Name) and node.id not in variables:
            raise ValueError(
                f"private_ip_formula references unknown name "
                f"{node.id!r}: {formula!r} (allowed: {sorted(variables)})"
            )
        if isinstance(node, ast.Constant):
            # bool is a subclass of int, so it sneaks past int|str. Reject
            # explicitly so True/False can't masquerade as 1/0 in formulas.
            if isinstance(node.value, bool) or not isinstance(node.value, int | str):
                raise ValueError(
                    f"private_ip_formula constants must be int or str, got "
                    f"{type(node.value).__name__}: {formula!r}"
                )

    return str(_formula_walk(tree, variables))


def _resolve_cidr_ip(
    cidr: str,
    ordinal: str,
    assignment: str | None,
    formula: str | None,
    marker: Mapping[str, Any],
) -> str:
    """Compute the per-ordinal IP from a host's ``private_ip_cidr``.

    ``in_order`` (default): network address + 10 + int(ordinal). Reserves
    .0–.9 for gateway and infrastructure.

    ``calculated``: requires ``private_ip_formula``. Variables exposed:
    ``ordinal: int``, ``env_name: str``, ``region_id: int`` (derived from
    the marker's ``network.ip_range`` per the per-region /16 convention).
    """
    network = ipaddress.ip_network(cidr, strict=True)
    try:
        ord_int = int(ordinal)
    except ValueError as exc:
        raise ValueError(f"ordinal must be a digit string, got {ordinal!r}") from exc

    strategy = (assignment or "in_order").strip().lower()
    if strategy == "in_order":
        host_index = 10 + ord_int
        addr = network.network_address + host_index
        if addr not in network:
            raise ValueError(f"in_order ordinal {ordinal!r} overflows {cidr} (would assign {addr})")
        return str(addr)

    if strategy == "calculated":
        if not formula:
            raise ValueError("private_ip_assignment_type='calculated' requires private_ip_formula")
        env_name_raw = marker.get("env_name") if isinstance(marker, Mapping) else None
        env_name = env_name_raw if isinstance(env_name_raw, str) else ""
        region_id = _derive_region_id(marker)
        rendered = _eval_formula(
            formula,
            {"ordinal": ord_int, "region_id": region_id, "env_name": env_name},
        )
        # Validate the rendered expression as a real IP address before
        # returning. ip_address raises ValueError on garbage. Also enforce
        # that the result lies inside the declared private_ip_cidr — a
        # formula typo that produces a syntactically valid IP outside the
        # network would otherwise return silently.
        rendered_ip = ipaddress.ip_address(rendered)
        if rendered_ip not in network:
            raise ValueError(
                f"private_ip_formula produced {rendered} outside private_ip_cidr {cidr}"
            )
        return rendered

    raise ValueError(
        f"private_ip_assignment_type must be 'in_order' or 'calculated', got {assignment!r}"
    )


def _derive_region_id(marker: Mapping[str, Any]) -> int:
    """Return ``region_id`` derived from ``network.ip_range``.

    Convention (see examples/environment/otsinfra.yaml): per-region master
    /16 sits at ``10.(100 + region_id).0.0/16``. Pull the second octet and
    subtract 100 when ``network.ip_range`` follows the pattern. Returns 0
    when no convention-matching network block is present (eu-1 grandfather).
    """
    network = marker.get("network") if isinstance(marker, Mapping) else None
    if isinstance(network, Mapping):
        raw = network.get("ip_range")
        if isinstance(raw, str):
            try:
                net = ipaddress.ip_network(raw, strict=False)
            except ValueError:
                return 0
            octets = str(net.network_address).split(".")
            if len(octets) >= 2 and octets[0] == "10":
                try:
                    second = int(octets[1])
                except ValueError:
                    return 0
                # The convention places per-region /16s at 10.(100+id).0.0/16.
                # Anything below 100 (or the impossible >255) is a non-
                # conforming range — return 0 rather than emit a negative
                # region_id that would silently corrupt formula output.
                if 100 <= second <= 255:
                    return second - 100
    return 0


def get_host_ip(marker: dict, role: str, ordinal: str = "01") -> str | None:
    """Resolve the per-ordinal private IP for *role* from loaded marker data.

    Resolution order (first non-empty wins):

    1. ``hosts.<role>.ordinals.<ordinal>.private_ip_address`` — explicit
       per-replica override.
    2. ``hosts.<role>.private_ip_address`` (legacy scalar) — only when
       ordinal is ``"01"`` so a marker carrying a single scalar continues
       to work for the canonical first instance and returns ``None`` for
       higher ordinals instead of silently aliasing.
    3. ``hosts.<role>.private_ip_cidr`` + assignment strategy — computed.

    Returns ``None`` when no source is configured. Callers that require
    a value must check and fail loud at their layer.
    """
    hosts = marker.get("hosts", {})
    if not isinstance(hosts, dict):
        # Malformed marker (hosts: as a list/scalar) — fail soft so callers
        # see the same "no IP configured" signal as a missing host entry.
        return None
    host = hosts.get(role, {})
    if not isinstance(host, dict):
        return None

    ordinals = host.get("ordinals")
    if isinstance(ordinals, dict):
        per_ord = ordinals.get(ordinal)
        if isinstance(per_ord, dict):
            override = per_ord.get("private_ip_address")
            if isinstance(override, str) and override:
                return override

    legacy = host.get("private_ip_address")
    if isinstance(legacy, str) and legacy and ordinal == "01":
        return legacy

    cidr = host.get("private_ip_cidr")
    if isinstance(cidr, str) and cidr:
        assignment = host.get("private_ip_assignment_type")
        formula = host.get("private_ip_formula")
        return _resolve_cidr_ip(
            cidr,
            ordinal,
            assignment if isinstance(assignment, str) else None,
            formula if isinstance(formula, str) else None,
            marker,
        )

    return None


def resolve_env_name(marker: Mapping[str, Any] | None) -> str:
    """Return the env name for the current shell, fail loud on conflict.

    Precedence (Option C from the issue #59 follow-on contract):

    * ``marker['env_name']`` AND ``$ENV_NAME`` set → MUST be equal;
      mismatch raises :class:`ots_shared.ssh.hostname.EnvNameConflict`.
    * Only ``marker['env_name']`` set → use it.
    * Only ``$ENV_NAME`` set OR neither set → marker is required;
      raise :class:`ots_shared.ssh.hostname.MarkerEnvNameMissing`.

    The marker is the source of truth; ``$ENV_NAME`` is the
    operator-supplied cross-check (typically set by direnv inside the
    environment dir). This helper exists so every consumer agrees on
    the precedence, instead of each CLI re-deriving it from
    ``os.environ`` and ``Path.cwd().name``.

    *marker* is typed permissively (``Mapping[str, Any] | None``) because
    callers pass in raw YAML loads — anything from a missing file
    (``None``) to a malformed scalar — and the function must coerce that
    to a typed error rather than a TypeError.
    """
    marker_value: str | None = None
    if isinstance(marker, Mapping):
        raw = marker.get("env_name")
        if isinstance(raw, str) and raw:
            marker_value = raw

    env_value = os.environ.get("ENV_NAME") or None

    if marker_value is None:
        # The contract: marker is required (even when $ENV_NAME is set).
        raise MarkerEnvNameMissing(
            "marker has no 'env_name' field; add `env_name: <env>` to otsinfra.yaml "
            "(operators may also set $ENV_NAME, but the marker is the source of truth)"
        )

    if env_value is not None and env_value != marker_value:
        raise EnvNameConflict(
            f"$ENV_NAME={env_value!r} disagrees with marker env_name={marker_value!r}; "
            "fix one or the other (the marker is the source of truth — adjust direnv "
            "or rerun in the matching environment dir)"
        )

    return marker_value


@dataclass(frozen=True, slots=True)
class MarkerNetwork:
    """Top-level ``network:`` block in ``otsinfra.yaml``.

    Holds the per-environment Hetzner network identity used by
    ``lots hcloud network ensure`` and consumed as a default by
    ``lots hcloud server create``. Semantic validation of the CIDR /
    zone is the caller's responsibility — this dataclass only enforces
    that each field is a non-empty string.
    """

    name: str
    ip_range: str
    network_zone: str


def get_network(marker: dict) -> MarkerNetwork | None:
    """Return the top-level ``network:`` block as ``MarkerNetwork``.

    Returns ``None`` when the block is absent (the caller decides
    whether that's an error). Raises :class:`KeyError` when a required
    key is missing and :class:`TypeError` when a value has the wrong
    type — Python conventional split, so callers always see a
    well-typed object or one of those two errors.
    """
    block = marker.get("network")
    if block is None:
        return None
    if not isinstance(block, dict):
        raise TypeError(f"'network' must be a mapping, got {type(block).__name__}: {block!r}")

    def _require_str(key: str) -> str:
        if key not in block:
            raise KeyError(f"network.{key} is required but missing")
        value = block[key]
        if not isinstance(value, str) or not value:
            raise TypeError(
                f"network.{key} must be a non-empty str, got {type(value).__name__}: {value!r}"
            )
        return value

    return MarkerNetwork(
        name=_require_str("name"),
        ip_range=_require_str("ip_range"),
        network_zone=_require_str("network_zone"),
    )


def generate_gitignore() -> str:
    """Generate ``.gitignore`` content for an OTS environment directory.

    Note: ``.trust/`` has its own ``.gitignore`` (created by init) so
    it's self-contained and can be deleted in one go.
    """
    lines = [
        ".envrc",
        "*.env",
        "config/",
        "gitrepo/",
        "history.db",
        "",
    ]
    return "\n".join(lines)


def create_gitignore(directory: Path, *, force: bool = False) -> Path:
    """Create ``.gitignore`` in *directory*.

    Raises:
        FileExistsError: If the file already exists and *force* is False.
    """
    path = directory / GITIGNORE_FILENAME
    if path.exists() and not force:
        raise FileExistsError(f"{path} already exists (use --force to overwrite)")
    path.write_text(generate_gitignore())
    return path


def _find_confexts(start: Path) -> Path | None:
    """Walk up from *start* looking for a sibling ``confexts/`` directory."""
    for parent in [start, *start.parents]:
        candidate = parent / "confexts"
        if candidate.is_dir():
            return candidate
    return None


def generate_envrc_template(
    cwd: Path | None = None,
    monorepo_root: Path | None = None,
) -> str:
    """Generate a template ``.envrc`` for a new OTS environment.

    Args:
        cwd: Directory where ``lots init`` is being run. Defaults to
            ``Path.cwd()``. Used to derive ``ENV_NAME``.
        monorepo_root: Base directory containing ``confexts/``. When
            ``None``, resolved by walking up from this file's location,
            then from *cwd*. Falls back to a placeholder string if not found.

    Deploy SSH keys are generated by init in ``.trust/deploy/``. The
    public half is read into ``DEPLOY_SSH_PUBKEY`` for cloud-init; the
    private path is set in ``DEPLOY_SSH_KEYFILE`` for SSH config.
    """
    resolved_cwd = cwd if cwd is not None else Path.cwd()
    env_name = resolved_cwd.name or "default"

    # Resolve confexts path: prefer monorepo_root arg, then walk from cwd,
    # then fall back to a placeholder.
    confexts_path: str
    if monorepo_root is not None:
        confexts_path = str(monorepo_root / "confexts")
    else:
        found = _find_confexts(resolved_cwd.resolve())
        confexts_path = str(found) if found else "<path-to-monorepo>/confexts"

    # Deploy keypair paths (generated by init in .trust/deploy/)
    deploy_keyfile = resolved_cwd / ".trust" / "deploy" / "ssh"
    deploy_pubkey_file = resolved_cwd / ".trust" / "deploy" / "ssh.pub"

    # SSH config path (generated by init in .ssh/)
    ssh_config_path = resolved_cwd / ".ssh" / "config"

    lines = [
        f"# {resolved_cwd / ENVRC_FILENAME}",
        "source_up",
        "",
        "# .envrc — otsinfra environment",
        f"# Created: {date.today().isoformat()}",
        "",
        'export HCLOUD_TOKEN="<paste-token-here>"',
        'export CLOUDFLARE_API_TOKEN="<paste-token-here>"',
        "",
        "# The debian system password for the regular user to be able",
        "# to sudo. Must be populated before `lots cloudinit generate`.",
        "#",
        "# Generate with: mkpasswd -m sha-512",
        'export DEPLOY_USER_PASSWD_HASH="<paste-hash-here>"',
        "",
        "# Deploy SSH keypair (generated by lots init in .trust/deploy/)",
        f'export DEPLOY_SSH_PUBKEY="$(cat {deploy_pubkey_file})"',
        f'export DEPLOY_SSH_KEYFILE="{deploy_keyfile}"',
        "",
        'export DEPLOY_USER="debian"',
        'export SSH_PORT="22"',
        'export JURISDICTION=""',
        f'export ENV_NAME="{env_name}"',
        "",
        "# Per-role tree consumed by lots confext push and lots provision push.",
        "# Point at the monorepo confexts/ directory or a standalone checkout.",
        f'export PROVISION_REPO="{confexts_path}"',
        "",
        "# Generated into ${PWD}/.ssh/config by default, but can be moved",
        "# anywhere as long as the paths in the SSH config are updated.",
        f'export SSH_CONFIG="{ssh_config_path}"',
        "",
        "# Host IPs defined in otsinfra.yaml",
        "",
    ]
    return "\n".join(lines)


def create_envrc_template(directory: Path, *, force: bool = False) -> Path:
    """Create a template ``.envrc`` in *directory*.

    Raises:
        FileExistsError: If the file already exists and *force* is False.
    """
    path = directory / ENVRC_FILENAME
    if path.exists() and not force:
        raise FileExistsError(f"{path} already exists (use --force to overwrite)")
    path.write_text(generate_envrc_template(cwd=directory))
    return path


def load_env_file(path: Path) -> dict[str, str]:
    """Parse a .otsinfra.env file into a dict.

    Format: KEY=VALUE lines. Blank lines and lines starting with # are
    ignored. Values are stripped of surrounding whitespace. Quoted values
    (single or double) are unquoted.
    """
    result: dict[str, str] = {}
    for line in path.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        if "=" not in line:
            continue
        key, _, value = line.partition("=")
        key = key.strip()
        value = value.strip()
        # Strip matching quotes
        if len(value) >= 2 and value[0] == value[-1] and value[0] in ('"', "'"):
            value = value[1:-1]
        result[key] = value
    return result


def resolve_host(host_flag: str | None = None) -> str | None:
    """Determine the target host using the resolution priority chain.

    Priority:
        1. Explicit --host flag value
        2. OTS_HOST environment variable
        3. OTS_HOST from .otsinfra.env (walk-up discovery)
        4. None (local execution)
    """
    # 1. Explicit flag
    if host_flag:
        logger.debug("Host from --host flag: %s", host_flag)
        return host_flag

    # 2. Environment variable
    env_host = os.environ.get("OTS_HOST")
    if env_host:
        logger.debug("Host from OTS_HOST env var: %s", env_host)
        return env_host

    # 3. Walk-up .otsinfra.env
    env_path = find_env_file()
    if env_path:
        env_vars = load_env_file(env_path)
        file_host = env_vars.get("OTS_HOST")
        if file_host:
            logger.info("Host from %s: %s", env_path, file_host)
            return file_host

    # 4. Local
    return None


def resolve_config_dir(start: Path | None = None) -> Path | None:
    """Resolve the current config directory for a jurisdiction.

    Resolution order:
        1. ``$OTS_CONFIG_DIR`` environment variable (explicit override).
        2. ``config/`` sibling to a ``otsinfra.yaml`` or ``.otsinfra.env``
           marker (walk-up discovery confirms we're in an OTS environment).
        3. OTS_TAG from ``.otsinfra.env`` → versioned directory name
           (e.g. ``v0.24`` → ``config-v0.24``). Legacy path.

    Returns the directory path if it exists, None otherwise.
    """
    # 1. Explicit env var override
    env_config_dir = os.environ.get("OTS_CONFIG_DIR")
    if env_config_dir:
        p = Path(env_config_dir)
        if p.is_dir():
            logger.debug("Config dir from $OTS_CONFIG_DIR: %s", p)
            return p
        logger.warning("$OTS_CONFIG_DIR set but not a directory: %s", p)

    # 2. Walk up to find the environment marker, then check for sibling config/
    marker_path = find_marker(start)
    if marker_path is not None:
        config_dir = marker_path.parent / _CONFIG_SYMLINK
        if config_dir.is_dir():
            logger.debug("Config dir from %s sibling: %s", marker_path.name, config_dir)
            return config_dir

    # 3. Legacy: derive from OTS_TAG in .otsinfra.env
    env_path = find_env_file(start)
    if env_path is None:
        return None

    env_vars = load_env_file(env_path)
    tag = env_vars.get("OTS_TAG")
    if not tag:
        logger.debug("No OTS_TAG in %s", env_path)
        return None

    version = _tag_to_version(tag)
    if version is None:
        logger.warning("Cannot parse version from OTS_TAG=%s in %s", tag, env_path)
        return None

    config_dir = env_path.parent / f"{_CONFIG_DIR_PREFIX}{version}"
    if config_dir.is_dir():
        logger.debug("Config dir from %s: %s", env_path, config_dir)
        return config_dir

    logger.debug("Config dir does not exist: %s", config_dir)
    return None


def generate_env_template(
    host: str = "",
    tag: str = "",
    repository: str = "",
    rabbitmq_url: str = "",
    sidecar_host_id: str = "",
) -> str:
    """Generate a .otsinfra.env template with optional pre-filled values.

    Returns the file content as a string.
    """
    lines = [
        "# .otsinfra.env — targeting context for OTS remote operations",
        "#",
        "# Walk-up discovery: rots commands search for this file",
        "# starting from the current directory upward to the repo root.",
        "",
        "# --- SSH Targeting (required) ---",
        f"OTS_HOST={host}",
        "",
        "# --- Container Image (required for deploy ops) ---",
        f"OTS_TAG={tag}",
    ]
    if repository:
        lines.append(f"OTS_REPOSITORY={repository}")

    # Sidecar section
    lines.extend(
        [
            "",
            "# --- Sidecar Configuration (optional) ---",
            "# RABBITMQ_URL: Connection string for sidecar queue communication",
            "# Format: amqp://user:pass@host:port/vhost",
        ]
    )
    if rabbitmq_url:
        lines.append(f"RABBITMQ_URL={rabbitmq_url}")
    else:
        lines.append("# RABBITMQ_URL=amqp://onetimesecret:PASS@db-host:5672/onetimesecret")

    lines.extend(
        [
            "",
            "# SIDECAR_HOST_ID: Identifier for per-host queue binding",
            "# Defaults to socket.gethostname() if not set",
        ]
    )
    if sidecar_host_id:
        lines.append(f"SIDECAR_HOST_ID={sidecar_host_id}")
    else:
        lines.append("# SIDECAR_HOST_ID=")

    lines.append("")
    return "\n".join(lines)


def validate_env_file(path: Path) -> tuple[list[str], list[str]]:
    """Validate a .otsinfra.env file for completeness.

    Returns:
        Tuple of (warnings, errors) — empty lists if valid.
    """
    warnings: list[str] = []
    errors: list[str] = []

    if not path.is_file():
        errors.append(f"Environment file not found: {path}")
        return warnings, errors

    env_vars = load_env_file(path)

    # Required for SSH targeting
    if not env_vars.get("OTS_HOST"):
        errors.append("OTS_HOST is required for remote operations")

    # Required for container operations
    if not env_vars.get("OTS_TAG"):
        warnings.append("OTS_TAG not set — container operations may use defaults")

    # Optional but recommended for sidecar
    if not env_vars.get("RABBITMQ_URL"):
        warnings.append("RABBITMQ_URL not set — sidecar will use server defaults")

    # SIDECAR_HOST_ID is optional — falls back to socket.gethostname()

    return warnings, errors


def _tag_to_version(tag: str) -> str | None:
    """Extract major.minor version from a tag string.

    Accepts formats like ``v0.24``, ``v0.24.1``, ``0.24``, ``0.24.1``.
    Returns ``"0.24"`` (major.minor only) or None if unparseable.
    """
    m = re.match(r"v?(\d+\.\d+)", tag)
    return m.group(1) if m else None


